#include "libft.h"

/*
** deplace |0123
**         |00123
*/

void	ft_deplace(char (*lst)[BUF_S])
{
	int		i;

	i = ft_strlen(*lst);
	while (--i >= 0)
		(*lst)[i + 1] = (*lst)[i];
	(*lst)[0] = '0';
}
